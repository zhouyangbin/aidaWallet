/**
 *
 * @param {string} data
 * @param {string} type
 */
const parseHexValue = (data, type) => {
  const lowerType = type.toLowerCase();
  if (lowerType == "string") {
    return data;
  } else if (lowerType == "number") {
    return parseInt(data, 16);
  } else if (lowerType == "address") {
    let zeroStart = 0;
    for (let i = 0; i < data.length; i++) {
      if (data[i] != "0") {
        zeroStart = i;
        break;
      }
    }

    return "0x" + data.substring(zeroStart);
  } else {
    return data;
  }
};

/**
 *
 * @param {string} data
 * @param {{name: string, index: number, type: string}[]} parseOption
 * @return {string[] | object}
 */
export const parseEthereumEventData = (data, parseOption) => {
  try {
    let rawData = data;
    if (data.indexOf("0x") != -1 || data.indexOf("0X") != -1) {
      rawData = data.substring(2);
    }

    let splitArray = [];
    for (let i = 0; i < rawData.length; i += 64) {
      const substr = rawData.substring(i, i + 64);
      splitArray.push(substr);
    }

    if (parseOption == null) {
      return splitArray;
    }

    let parseResult = {};
    for (let item of parseOption) {
      const itemData = splitArray[item.index];
      parseResult[item.name] = parseHexValue(itemData, item.type);
    }

    return parseResult;
  } catch (e) {
    console.error(e);
    throw e;
  }
};
