export const handlePromiseAll = promiseArray => {
  // console.log(promiseArray, "pro,ose");
  return new Promise((resolve, reject) => {
    if (!Array.isArray(promiseArray)) {
      return reject(new Error("传入的参数必须是数组"));
    }
    const res = [];
    const promiseLen = promiseArray.length;
    let counter = 0;
    for (let i = 0; i < promiseLen; i++) {
      Promise.resolve(promiseArray[i])
        .then(value => {
          counter++;
          res[i] = value;
          if (counter === promiseLen) {
            resolve(res);
          }
        })
        .catch(e => reject(e));
    }
  });
};

export const handleMoneyFormatter = (num, digits, decimal) => {
  if (decimal) {
    num = num / 10 ** decimal;
  } else {
    num = num / 10 ** 18;
  }
  const si = [
    { value: 1, symbol: "" },
    // { value: 1e3, symbol: "K" },
    { value: 1e6, symbol: "M" },
    // { value: 1e9, symbol: "G" },
    // { value: 1e12, symbol: "T" },
    // { value: 1e15, symbol: "P" },
    { value: 1e18, symbol: "E" },
  ];
  const rx = /\.0+$|(\.[0-9]*[1-9])0+$/;
  let i;
  for (i = si.length - 1; i > 0; i--) {
    if (num >= si[i].value) {
      break;
    }
  }
  return (num / si[i].value).toFixed(digits).replace(rx, "$1") + si[i].symbol;
};

